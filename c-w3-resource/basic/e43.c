#include <stdio.h>

int main(void)
{
    int a,b;
    int c;

    printf("Input no of lines:");
    scanf("%i", &a);

    printf("Input no of characters:");
    scanf("%i", &b);

    for (int i = 1; i <= a; i++)
    {
        for (int j = c; j <= b; j++)
        {
            printf("%i", c );
            c = j;
        }
        printf("\n");
    }
}
